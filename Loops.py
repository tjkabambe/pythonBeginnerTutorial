# Create a list
my_list = [2, 4, 6, 8, 10, 12]

# Add 2 to each element in the list
# We'll use the 'for' loop
# With Loops, must always add a colon ':' at the end
for element in my_list:
    element = element + 3
    print(element) # loop is being executed until the elements are finished.

